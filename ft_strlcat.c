#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;

	j = 0;
	i = ft_strlen(src);
	while (j < size && src[j])
		dst[i++] = src[j++];
	return(size);
}
